#include "libft.h"
#include "minishell.h"

bool	ft_is_metacharacter(char c)
{
	if (c == ' ' || c == '\n' || c == '\t' || c == '|' ||
			c == '&' || c == ';' || c == '(' || c == ')'
			|| c == '<' || c == '>')
		return (true);
	return (false);
}